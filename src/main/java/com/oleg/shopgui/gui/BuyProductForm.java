    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Product product = jList1.getSelectedValue();        
        int count = listTransaction.countProducts(product)+1;                
        if (product.getQtyOnHand() < (product.getMinOrderQty() * (count))) {
            JOptionPane.showMessageDialog(rootPane, 
                    "Sorry, not enough products to sell", "Information", 
                    JOptionPane.INFORMATION_MESSAGE);          
        } else {
            listTransaction.add(product);
            JOptionPane.showMessageDialog(rootPane, "Product added",
                    "Information", JOptionPane.INFORMATION_MESSAGE);
            
            DefaultComboBoxModel<Product> model = (DefaultComboBoxModel<Product>) jList2.getModel();           
            model.addElement(product);
            if (listTransaction.isFull()){
                this.dispose();
            }            
        }
                               
    }//GEN-LAST:event_jButton1ActionPerformed

