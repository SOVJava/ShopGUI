    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        for (int i = 0; i < list.length(); i++) {
            for (int j = 0; j < listTransaction.length(); j++) {
                if (list.get(i).getName().equals(listTransaction.get(j).getName()))
                    list.get(i).setQtyOnHand(list.get(i).getQtyOnHand()-
                            list.get(i).getMinOrderQty());
            }                       
        }
        JOptionPane.showMessageDialog(rootPane, "Thank you for your purchase", 
                "Infirmation", JOptionPane.INFORMATION_MESSAGE);
        listTransaction.clean();
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed


