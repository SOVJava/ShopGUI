    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int index = jList1.getSelectedIndex();
        listTransaction.remove(index);
        JOptionPane.showMessageDialog(rootPane, "The product is removed", 
                "Information", JOptionPane.INFORMATION_MESSAGE);        
        DefaultComboBoxModel<Product> model = (DefaultComboBoxModel<Product>) jList1.getModel();
        model.removeElementAt(index);        
        if (listTransaction.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "No products added", 
                    "Information", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed


