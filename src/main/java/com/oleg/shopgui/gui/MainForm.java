    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (list.isFull()){
            JOptionPane.showMessageDialog(rootPane, "Stock is full", 
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        new RegisterProductForm(this.list).setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (list.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Stock is empty", 
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (listTransaction.isFull()){
            JOptionPane.showMessageDialog(rootPane, "You have already added 3 products", 
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        new BuyProductForm(list, listTransaction).setVisible(true);
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (listTransaction.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "No products added", 
                    "Information", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        new RemoveProductForm(listTransaction).setVisible(true);
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (list.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Stock is empty", 
                    "Information", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        new StockViewForm(list).setVisible(true);
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (listTransaction.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Stock is empty", 
                    "Information", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        new CheckOutForm(list, listTransaction).setVisible(true);
    }//GEN-LAST:event_jButton5ActionPerformed

