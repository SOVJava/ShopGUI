    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            String name = jTextField1.getText();           
            if (name.length() < 3 || name.length() > 25) {              
                JOptionPane.showMessageDialog(rootPane, 
                        "Invalid input, enter a NAME between 3 and 25 characters", 
                        "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }                      
            if (list.isDuplicated(name)) {                
                JOptionPane.showMessageDialog(rootPane, "incorrect, duplicated", 
                        "Error", JOptionPane.ERROR_MESSAGE);
                return;  
            }
                        
            String desc = jTextField2.getText();
            if (desc.length() < 1 || desc.length() > 50){
                JOptionPane.showMessageDialog(rootPane, 
                        "Invalid input, enter a DESC between 1 and 50 characters", 
                        "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            double price = Double.valueOf(jTextField3.getText());
            if (price<0){
                JOptionPane.showMessageDialog(rootPane, "Incorrect input, PRICE < 0", 
                        "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            int qtyOnHand = RandomNumberGenerator.genInt(0,10);
            int minOrderQty = RandomNumberGenerator.genInt(1,5);
            Product product = new Product(name,desc,qtyOnHand,price,minOrderQty);
            this.list.add(product);
            jButton2ActionPerformed(evt);
            JOptionPane.showMessageDialog(rootPane, "Product added", 
                    "Information", JOptionPane.INFORMATION_MESSAGE);
            if (list.isFull()){
                JOptionPane.showMessageDialog(rootPane, "Stock is full", 
                        "Information", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            }
                               
        } catch(NumberFormatException e){
            JOptionPane.showMessageDialog(rootPane, "Incorrect price", "Error", 
                    JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

